# REPLIT_FRICTION_LOG
# -----------------------------------------------------------------------------
# 1) Import URL shape
#    Spec: open https://replit.new/{github-url}. Official docs only describe
#    https://replit.com/github.com/owner/repo for "rapid import". Tried curl -I on
#    https://replit.new/github.com/owner/repo — HTTP 302 via new-redirector-util.replit.app.
#    Embedding a full URL as one path segment (URL-encoded https://...) also 302'd but
#    produced an awkward //https://... location. Chose replit.new/github.com/owner/repo
#    parsed from the remote so the browser opens a single stable pattern; noted mismatch
#    with literal "{full github-url}" in the spec.
#
# 2) replit.toml vs .replit
#    User asked for "replit.nix or replit.toml". Replit's configuration doc names .replit
#    (TOML, often hidden) plus replit.nix; no first-party "replit.toml" file in that doc.
#    Did not create replit.toml to avoid an undocumented duplicate config surface; only
#    .replit + replit.nix are generated when missing.
#
# 3) Pinned python module id
#    Docs example uses modules = ["python-3.10:v18-20230807-322e88b"]. Replit may rotate
#    these strings; there is no CLI/API in-repo to resolve the latest id, so we copied the
#    documented example and accept that it may need manual refresh if imports fail.
#
# 4) Run command vs packaged install
#    Gateway entry in this repo is uvicorn app.main:app (see scripts/dev-restart.sh, README).
#    For GitHub import, dependencies come from pyproject.toml; used `pip install -e .` on
#    onBoot plus uvicorn on Run so the repl matches editable dev layout without publishing
#    to PyPI inside the repl.
#
# 5) Node / monorepo
#    package.json lives under frontend/ only; gateway is Python-only. Did not add Node to
#    replit.nix since the stated goal is the Free Agents API gateway, not the Next.js app.
#
# 6) Git on Replit: stale lock + blocked .git writes + broken .replit during rebase
#    After pull/rebase/merge, an empty or stale lock under .git (e.g. index.lock) can block
#    every git command until removed manually in Shell. Separately, the Repl environment
#    sometimes refuses writes to .git (IDE Git panel then can't fetch/commit); only Shell
#    may work, or a Repl restart may be needed. Merge conflict markers inside .replit break
#    TOML parsing → Nix environment fails to build; fix file then git rebase --abort or
#    reset --hard origin/main. Replit Git UI shows "Unsupported state: middle of a rebase"
#    until rebase is finished or aborted. No Replit API surfaced in-product to clear locks.
# -----------------------------------------------------------------------------
