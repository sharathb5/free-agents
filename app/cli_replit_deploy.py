def _run_git(args: list[str], *, cwd: Path) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        ["git", *args],
        cwd=str(cwd),
        capture_output=True,
        text=True,
        check=False,
    )


def _ensure_git_repo(root: Path) -> None:
    git_dir = root / ".git"
    probe = _run_git(["rev-parse", "--git-dir"], cwd=root)
    if probe.returncode != 0 or not git_dir.exists():
        print("Error: not a git repository (no .git or git rev-parse failed).", file=sys.stderr)
        if probe.stderr.strip():
            print(probe.stderr.strip(), file=sys.stderr)
        sys.exit(2)


def _normalize_git_remote_to_https(url: str) -> Optional[str]:
    u = url.strip()
    if not u:
        return None
    # git@github.com:org/repo.git
    if u.startswith("git@github.com:"):
        path = u.split(":", 1)[1].removesuffix(".git")
        return f"https://github.com/{path}"
    if u.startswith("ssh://git@github.com/"):
        path = u.removeprefix("ssh://git@github.com/").removesuffix(".git").lstrip("/")
        return f"https://github.com/{path}"
    if "github.com" not in u.lower():
        return None
    if u.startswith("git://github.com/"):
        path = u.removeprefix("git://github.com/").removesuffix(".git")
        return f"https://github.com/{path}"
    if u.startswith("https://github.com/") or u.startswith("http://github.com/"):
        parsed = urlparse(u)
        if not parsed.path or parsed.path == "/":
            return None
        path = parsed.path.strip("/").removesuffix(".git")
        if not re.fullmatch(r"[^/]+/[^/]+", path):
            return None
        return f"https://github.com/{path}"
    return None


def _parse_git_remote_v_fetch(line: str) -> Optional[Tuple[str, str]]:
    """Return (remote_name, url) for lines like 'origin  https://github.com/a/b.git (fetch)'."""
    s = line.strip()
    if not s.endswith("(fetch)"):
        return None
    core = s[: s.rindex("(fetch)")].strip()
    bits = core.split(None, 1)
    if len(bits) != 2:
        return None
    return bits[0], bits[1]


def _discover_github_https_url(root: Path) -> str:
    proc = _run_git(["remote", "-v"], cwd=root)
    if proc.returncode != 0:
        print("Error: `git remote -v` failed.", file=sys.stderr)
        print(proc.stderr.strip(), file=sys.stderr)
        sys.exit(2)
    candidates: list[Tuple[int, str]] = []
    for line in proc.stdout.splitlines():
        parsed = _parse_git_remote_v_fetch(line)
        if not parsed:
            continue
        name, raw_url = parsed
        url = _normalize_git_remote_to_https(raw_url)
        if not url:
            continue
        priority = 0 if name == "origin" else 1
        candidates.append((priority, url))
    if not candidates:
        print(
            "Error: no GitHub remote found. Add one with: git remote add origin https://github.com/ORG/REPO.git",
            file=sys.stderr,
        )
        sys.exit(2)
    candidates.sort(key=lambda x: (x[0], x[1]))
    return candidates[0][1]


def _owner_repo(github_https: str) -> Tuple[str, str]:
    # https://github.com/o/r -> (o, r)
    path = urlparse(github_https).path.strip("/").removesuffix(".git")
    parts = path.split("/")
    if len(parts) != 2:
        print(f"Error: could not parse owner/repo from {github_https!r}", file=sys.stderr)
        sys.exit(2)
    return parts[0], parts[1]


def _replit_import_url(github_https: str) -> str:
    owner, repo = _owner_repo(github_https)
    return f"{REPLIT_NEW_BASE}/github.com/{owner}/{repo}"


def _write_if_absent(path: Path, content: str, label: str) -> None:
    if path.exists():
        print(f"Keep existing {label}: {path}")
        return
    path.write_text(content, encoding="utf-8")
    print(f"Wrote {label}: {path}")


def _maybe_write_replit_nix(root: Path) -> None:
    nix = root / "replit.nix"
    toml = root / "replit.toml"
    if nix.exists() or toml.exists():
        if nix.exists():
            print(f"Keep existing replit.nix: {nix}")
        if toml.exists():
            print(f"Keep existing replit.toml: {toml}")
        return
    nix.write_text(REPLIT_NIX_TEMPLATE, encoding="utf-8")
    print(f"Wrote replit.nix: {nix}")


