def run_deploy_replit(*, root: Optional[Path] = None) -> None:
    """Prepare Replit config files and open the import URL. Mutates cwd’s project root only."""
    root = (root or Path.cwd()).resolve()
    _ensure_git_repo(root)
    github_https = _discover_github_https_url(root)
    print(f"GitHub remote: {github_https}")

    _write_if_absent(root / ".replit", DOT_REPLIT_TEMPLATE, ".replit")
    _maybe_write_replit_nix(root)

    _print_replit_secrets()

    open_url = _replit_import_url(github_https)
    print(f"Opening browser: {open_url}")
    webbrowser.open(open_url)

    print()
    print("After Replit opens:")
    print("Step 1: Add your secrets in Replit (Tools → Secrets)")
    print("Step 2: Click Run")
    print("Step 3: Your Free Agents gateway is live")
    print()
